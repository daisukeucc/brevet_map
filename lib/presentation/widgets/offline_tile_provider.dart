import 'dart:io';
import 'dart:typed_data';
import 'package:flutter/painting.dart';
import 'package:flutter_map/flutter_map.dart';

/// ローカルに保存済みタイルがあればそれを返す TileProvider。
/// タイルが見つからない場合は透明タイルを返す（ネットワークアクセスしない）。
class OfflineTileProvider extends TileProvider {
  OfflineTileProvider(this.offlineDirPath);

  final String offlineDirPath;

  /// 1×1 透明 PNG（ダウンロード範囲外のタイルに使用）
  static final Uint8List _emptyTile = Uint8List.fromList(const [
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00,
    0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01,
    0x00, 0x00, 0x00, 0x01, 0x08, 0x06, 0x00, 0x00, 0x00, 0x1F,
    0x15, 0xC4, 0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41,
    0x54, 0x78, 0x9C, 0x62, 0x00, 0x01, 0x00, 0x00, 0x05, 0x00,
    0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
    0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
  ]);

  @override
  ImageProvider<Object> getImage(
      TileCoordinates coordinates, TileLayer options) {
    final file = File(
        '$offlineDirPath/${coordinates.z}_${coordinates.x}_${coordinates.y}.png');
    if (file.existsSync()) return FileImage(file);
    return MemoryImage(_emptyTile);
  }
}
